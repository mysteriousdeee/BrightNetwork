print("xtension Task 5: Calculate the average salary for people over 30 years of age")

# Filter employees over 30 years of age and calculate the average salary
older_employees = [employee for employee in employees if employee["age"] > 30]
total_salary = sum(employee["salary"] for employee in older_employees)
average_salary = total_salary / len(older_employees)

# Print the average salary for people over 30 years of age
print(
    f"The average salary for people over 30 years of age is {average_salary:.2f}.")


