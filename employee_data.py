print("Task 4: Compare total salaries of Product and Business departments")

# Calculate total salaries for Product and Business departments
product_department_salaries = sum(
    employee["salary"] for employee in employees if employee["department"] == "Product")
business_department_salaries = sum(
    employee["salary"] for employee in employees if employee["department"] == "Business")

# Compare the total salaries and print the result
if product_department_salaries > business_department_salaries:
    print("Product department costs more for the company.")
else:
    print("Business department costs more for the company.")


