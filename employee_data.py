print("Extension Task 6: Count employees with certain job titles")

# Create a dictionary to store job title counts
job_title_counts = {}

# Count employees with certain job titles
for employee in employees:
    job_title = employee["job_title"]
    if job_title in job_title_counts:
        job_title_counts[job_title] += 1
    else:
        job_title_counts[job_title] = 1

# Print the job title counts
print("Job title counts:")
for job_title, count in job_title_counts.items():
    print(f"{job_title}: {count}")
